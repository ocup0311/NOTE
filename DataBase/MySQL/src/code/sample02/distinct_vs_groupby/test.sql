-- DISTINCT
SELECT DISTINCT k3 AS x
FROM Table1
WHERE k3 = k4
ORDER BY x;

-- GROUP BY
SELECT k3 AS x
FROM Table1
WHERE k3 = k4
GROUP BY x
ORDER BY x;

-- COUNT(*) >= 2
SELECT DISTINCT(k3) AS x
FROM Table1
WHERE k3 = k4
GROUP BY k3
HAVING COUNT(*) >= 2
ORDER BY k3;

-- GROUP BY k3, k4
SELECT DISTINCT(k3) AS x
FROM Table1
WHERE k3 = k4
GROUP BY k3, k4
HAVING COUNT(*) >= 2
ORDER BY k3;

-- JOIN
SELECT DISTINCT T1.k3 AS x
FROM Table1 T1 JOIN Table1 T2
ON T1.k3 = T2.k4 AND T1.id = T2.id
ORDER BY x;

-- UNION
SELECT k3 AS x
FROM Table1
WHERE k3 = k4
UNION
SELECT k3 AS x
FROM Table1
WHERE k3 = k4
ORDER BY x;



