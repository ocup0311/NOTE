-- 當 row 佔比例很大
-- 只有 idx_k3

EXPLAIN 
SELECT k2
FROM Table1
WHERE k3 <> 3 OR k3 IS NULL;

EXPLAIN 
SELECT k2
FROM Table1
WHERE IFNULL(k3, -1) <> 3;

EXPLAIN 
SELECT k2
FROM Table1
WHERE CASE
    WHEN k3 IS NULL THEN 1
    WHEN k3 <> 3 THEN 1
    ELSE 0
END = 1;

EXPLAIN 
SELECT k2 FROM Table1 WHERE k3 <> 3
UNION ALL
SELECT k2 FROM Table1 WHERE k3 IS NULL;

EXPLAIN 
SELECT k2
FROM Table1
WHERE COALESCE(k3, -1) <> 3;

EXPLAIN 
SELECT k2
FROM Table1
WHERE NOT k3 <=> 3;


