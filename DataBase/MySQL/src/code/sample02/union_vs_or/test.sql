-- 測試 "OR" 與 "UNION" 使用情形

EXPLAIN ANALYZE
SELECT id, k3, k4 FROM Table1 WHERE k4 = 43
UNION
SELECT id, k3, k4 FROM Table1 WHERE k3 = 343;

EXPLAIN
SELECT id, k3, k4 FROM Table1 WHERE k4 = 43
UNION
SELECT id, k3, k4 FROM Table1 WHERE k3 = 343;

EXPLAIN ANALYZE SELECT id, k3, k4 FROM Table1 WHERE k4 = 43 OR k3 = 343;

EXPLAIN SELECT id, k3, k4 FROM Table1 WHERE k4 = 43 OR k3 = 343;


-- 測試更多 index_merge 使用情形

EXPLAIN SELECT k2 FROM Table1 WHERE k4 = 43 AND k3 = 343;
EXPLAIN SELECT k2 FROM Table1 WHERE k3 = 343 AND k4 = 43;


EXPLAIN ANALYZE SELECT k2 FROM Table1 WHERE k4 <> 4 OR k4 IS NULL;


